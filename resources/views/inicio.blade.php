    <h3>Cardonal</h3>
    <p>Este municipio se encuentra en los limites donde termina el Valle del Mezquital y comienza la Sierra Gorda; sus primeros pobladores fueron de origen indígena otomí-hñänhu. Posee paisajes contrastantes; los yacimientos metálicos encontrados durante la conquista provocaron la instalación de minas.</p>
    <p>Este lugar posee en su territorio una diversidad de climas que van desde el desierto con vegetación xerofita o el bosque de coníferas, hasta el subtropical en la barranca de Tolantongo, por donde pasa el Río Amajac, en donde el clima cálido y el agua dan lugar a una extensa y verde vegetación</p>
    <p>Cardonal ofrece a sus visitantes una especial joya colonial y artística de Hidalgo: el Santuario del Señor de Mapethé, ubicado en la población de Santuario, lugar en donde se ubicaban las minas del Real de Plomo Pobre y a donde fue llevado un Cristo que se hizo famoso por sus milagros. Además de su fastuosa fachada, la iglesia posee al interior cinco retablos barrocos cuyo color dorado ilumina el recinto y a ello se suman pinturas religiosas y especiales decorados en la bóveda y columnas del templo.</p>
    <p>Este sensacional lugar es famoso por sus artesanías de ixtle, fibra de maguey y lechuguilla, como son los ayates, así como jarciarías como las conocidas escobetillas de uso doméstico; además, existe el uso del telar de cintura y los tradicionales bordados otomíes en lana o algodón.</p>
    <p>En cuanto a la gastronomía, se producen y consumen múltiples derivados de maguey como los chinicuiles, los gualumbos, el jarabe de agave y el pan de pulque.</p>
